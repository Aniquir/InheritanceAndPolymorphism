package book;

public class Book {

    private String title;
    private int numerOfPages;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public int getNumerOfPages() {
        return numerOfPages;
    }

    public void setNumerOfPages(int numerOfPages) {
        this.numerOfPages = numerOfPages;
    }
}
